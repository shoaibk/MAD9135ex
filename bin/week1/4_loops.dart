// Objective: Practice using for and while loops.

// Task: Write a program that prints the numbers from 1 to 10 using both a for loop and a while loop.
void main(){
	// for loop
	for(int i = 1; i < 11; i++){
		print(i);
	}

	print("\n");

	// while loop
	int j = 1;
	while(j <= 10){
		print(j);
		j++;
	}
}
