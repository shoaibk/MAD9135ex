// Objective: Perform arithmetic operations and understand data types.

// Task: Create two integer variables and perform addition, subtraction, multiplication, and division.
// Print the results.
// Sample Output:
// Addition: 15
// Subtraction: 5
// Multiplication: 50
// Division: ...

void main() {
    var num1 = 10;
    var num2 = 5;
    var sum = num1 + num2;
    var sub = num1 - num2;
    var mul = num1 * num2;
    var div = num1 / num2;
    print("Addition: $sum");
    print("Subtraction: $sub");
    print("Multiplication: $mul");
    print("Division: $div");
}
