// Objective: Perform arithmetic operations and understand data types.

// Task: Create two integer variables and perform addition, subtraction, multiplication, and division.
// Print the results.
// Sample Output:
// Addition: 15
// Subtraction: 5
// Multiplication: 50
// Division: ...

void main() {
    int num1 = 10;
    int num2 = 20;
    var addition = num1 + num2;
    var subtraction = num1 - num2;
    var multiplication = num1 * num2;
    var division = num1 / num2;

    print("Addition: $addition");
    print("Subtraction: $subtraction");
    print("Multiplication: $multiplication");
    print("Division: $division");
}
